def country_name(code):
    if code == 'bg':
        return 'Болгария'
    elif code == 'gr':
        return 'Греция'
    elif code == 'ge':
        return 'Грузия'
    elif code == 'do':
        return 'Доминикана'
    elif code == 'eg':
        return 'Египет'
    elif code == 'il':
        return 'Израиль'
    elif code == 'id': # Indonesia
        return 'Индонезия'
    elif code == 'es':
        return 'Испания'
    elif code == 'it':
        return 'Италия'
    elif code == 'cy':
        return 'Кипр'
    elif code == 'cn': # China
        return 'Китай'
    elif code == 'cu': # Cuba
        return 'Куба'
    elif code == 'lv':
        return 'Латвия'
    elif code == 'lt':
        return 'Литва'
    elif code == 'mu': # Mauritius
        return 'Маврикий'
    elif code == 'mv': # Maldives
        return 'Мальдивы'
    elif code == 'mx':
        return 'Мексика'
    elif code == 'ae':
        return 'ОАЭ'
    elif code == 'pt':
        return 'Португалия'
    elif code == 'sc': # Seychelles
        return 'Сейшелы'
    elif code == 'th': # Thailand
        return 'Таиланд'
    elif code == 'tr':
        return 'Турция'
    elif code == 'fr':
        return 'Франция'
    elif code == 'lk': # Sri Lanka
        return 'Шри Ланка'
    elif code == 'ee':
        return 'Эстония'
    return 'Неизвестная страна'
